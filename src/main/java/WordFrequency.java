
public class WordFrequency {
    private String word; // word
    private int count;

    //    smell: naming
    public WordFrequency(String word, int count) {
        this.word = word;
        this.count = count;
    }

    public String getWord() {
        return this.word;
    }

    public int getWordCount() {
        return this.count;
    }


}
